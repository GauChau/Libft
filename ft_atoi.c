int	ft_atoi(const char *nptr)
{
	int	i;
	int	n;
	int	x;

	i = 0;
	x = 1;
	n = 0;
	while (nptr[i] == ' ')
		i++;
	if (nptr[i] == '-')
	{
		x = -x;
		i++;
	}
	else if (nptr[i] == '+')
			i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
		n = n * 10 + (nptr[i] - '0');
	return (x * n);
}
